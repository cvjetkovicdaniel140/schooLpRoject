import java.util.*;

public class Main {
    public static void main(String[] args) {
        // Example program to demonstrate Java's switch-case statement
        String color = "red";
        
        if (color.equals("red")) {
            System.out.println("The value of color is red.");
        } else if (color.equals("green")) {
            System.out.println("The value of color is green.");
        } else {
            System.out.println("Unknown color: " + color);
        }
    }
}
